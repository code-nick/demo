
// array push pop
// array shift unshift


let fruits = [ "apple" , "mango" , "grapes"];
console.log(fruits);

//push

fruits.push("banana");
console.log(fruits);

//pop

fruits.pop();
console.log(fruits);
let poppedfruit = fruits.pop();
console.log(fruits);
console.log("popped fruits is", poppedfruit);

//unshift

fruits.unshift("banana");
fruits.unshift("my name is vicky Vema");
console.log(fruits);


//shift 
let removedfruits = fruits.shift();
console.log(fruits);
console.log("removed fruits is " , removedfruits)
